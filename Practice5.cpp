#include <iostream>

using namespace std;

int main()
{
  int a = 10;
  switch(a) {
    case 10:
      cout << "Case B" << endl;
      break;
    case 20:
      cout << "Case C" << endl;
      break;
    default:
      cout << "Case D" << endl;
      break;
  }
}
